package org.example;

// this code aim is to show general data types in java.
public class Main {
    public static void main(String[] args) {
        String a = "hello world";
        char b = 'A'; //all unicode characters
        byte c = -127; //date types is integer
        short d = 32000;//data types is integer
        int e = -219090000;//data types is integer
        long f = -128734612;//data types is integer
        float i = -1283714f;//floating-point number
        double j = -1.3247129812730123809124902812034;//floating-point number
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(i);
        System.out.println(j);

    }
}